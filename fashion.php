                Western wear is a category of men's and women's clothing which derives its unique style from the clothes worn in the 19th century Wild West. It ranges from accurate historical reproductions of pioneer, mountain man, Civil War, cowboy and vaquero clothing to the stylized garments popularized by singing cowboys such as Gene Autry and Roy Rogers in the 1940s and 1950s. Western wear can be very informal, with a t-shirt and blue jeans forming a basic ensemble, or it may consist of tailored formal garments with western accents. 
