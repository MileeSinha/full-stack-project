                    In the 1960s and 1970s, at the same time as Western fashion was adopting elements of Indian dress, Indian fashion also absorbed elements of Western dress.[2][3] This practice of mutual appropriation continued throughout the 1980s and 1990s, as multiculturalism in fashion design took hold, with Western designers incorporating traditional Indian crafts, textiles and techniques at the same time as Indian designers allowed the West to influence their work.[2] While middle-class Indian women in migrant communities originally tended to favour Western styles for all occasions in the 1960s and 1970s, they gradually began to wear stylish Indian dress for special occasions as a status symbol equivalent to chic Western fashion.[4] One example of a traditionally Indian garment that has been heavily influenced by Western fashion is the kurta suit, a reversal of the established tradition of Western fashion being influenced by Asian design.[3] Geczy noted that an Indian woman wearing traditional clothing might find herself
                    
