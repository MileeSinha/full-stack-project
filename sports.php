            The game developed in British India from the earlier game of battledore and shuttlecock.
             European play came to be dominated by Denmark but the game has become very popular in Asia, with recent
              competitions dominated by China. Since 1992, badminton has been a Summer Olympic sport with four events: men's singles,
               women's singles, men's doubles, and women's doubles,[2] with mixed doubles added four years later. At high levels of play,
